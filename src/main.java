public class main {
    public static void main(String[] args) {
        System.out.println("Hello, world!");

        int[] integers = new int[20];
        for (int i = 0; i < integers.length; i++) {
            integers[i] = i + 1;
            System.out.print(integers[i] + " ");
        }

        System.out.println("");
    }
}
